    private void btn_introducirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_introducirActionPerformed
        // TODO add your handling code here:
        try{
            float numero = Float.parseFloat(txt_introducir.getText());
            txt_introducir.setText("");
            this.lista.add(numero);
            DefaultListModel modelo = (DefaultListModel) ls_mostrar.getModel();
            modelo.clear();
            for(Float num: this.lista){
                modelo.addElement(num);
            }
        }catch(NumberFormatException e){
            txt_introducir.setText("Error, introduce un numero");
        }
    }//GEN-LAST:event_btn_introducirActionPerformed

