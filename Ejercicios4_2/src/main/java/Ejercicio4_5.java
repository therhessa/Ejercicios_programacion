    private void btn_comprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_comprobarActionPerformed
        // TODO add your handling code here:
        List <Float> copia1 = new ArrayList<Float>(this.lista);
        Collections.sort(copia1, Collections.reverseOrder());
        if(this.lista.equals(copia1)){
            lbl_resultado.setText("descendente");
        }else{
            List <Float> copia2 = new ArrayList<Float>(this.lista);
            Collections.sort(copia2);
            if(this.lista.equals(copia2)){
                lbl_resultado.setText("ascendente");
            }else{
                lbl_resultado.setText("desordenada");
            }
        }
    }//GEN-LAST:event_btn_comprobarActionPerformed

