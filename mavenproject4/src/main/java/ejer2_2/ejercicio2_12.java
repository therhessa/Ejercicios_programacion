    private void btn_introducirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_introducirActionPerformed
        // TODO add your handling code here:
         try {
            int n = Integer.parseInt(txt_introducir.getText());
            if(n==0) {
                // imprimir resultado
                txt_mostrar.setText("Positivos = " + Integer.toString(cpos) + "\nNegativos = " + Integer.toString(cneg));
                cneg = 0;
                cpos = 0;
            } else {
                if(n<0) {
                    cneg++;
                } else {
                    cpos++;
                }
            }
        } catch (NumberFormatException e) {
            txt_mostrar.setText("No has introducido un nÃºmero");
        }
    }//GEN-LAST:event_btn_introducirActionPerformed

